CREATE TABLE IF NOT EXISTS customer (
	id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
	name VARCHAR(150) NOT NULL,
	email VARCHAR(255)
);

CREATE TABLE IF NOT EXISTS purchase_order (
	id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
	order_number VARCHAR(50) NOT NULL UNIQUE,
	customer_id BIGINT NOT NULL,
	order_date DATE NOT NULL,
	CONSTRAINT fk_po_customer
		FOREIGN KEY (customer_id) REFERENCES customer(id)
);

CREATE TABLE IF NOT EXISTS products (
	id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
	product_name VARCHAR(150) NOT NULL UNIQUE,
	description VARCHAR(255)
);

CREATE TABLE IF NOT EXISTS purchase_order_items (
	id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
	purchase_order_id BIGINT NOT NULL,
	product_id BIGINT NOT NULL,
	quantity INT NOT NULL,
	unit_price DECIMAL(12,2) NOT NULL,
	CONSTRAINT fk_po_items_order
		FOREIGN KEY (purchase_order_id) REFERENCES purchase_order(id)
		ON DELETE CASCADE,
	CONSTRAINT fk_po_items_product
		FOREIGN KEY (product_id) REFERENCES products(id),
	CONSTRAINT chk_po_item_quantity CHECK (quantity > 0),
	CONSTRAINT chk_po_item_unit_price CHECK (unit_price >= 0)
);

CREATE TABLE IF NOT EXISTS sales_invoice (
	id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
	invoice_number VARCHAR(50) NOT NULL UNIQUE,
	purchase_order_id BIGINT NOT NULL,
	customer_id BIGINT NOT NULL,
	invoice_date DATE NOT NULL,
	total_amount DECIMAL(12,2) NOT NULL,
	CONSTRAINT fk_invoice_order
		FOREIGN KEY (purchase_order_id) REFERENCES purchase_order(id),
	CONSTRAINT fk_invoice_customer
		FOREIGN KEY (customer_id) REFERENCES customer(id),
	CONSTRAINT chk_invoice_total_amount CHECK (total_amount >= 0)
);

CREATE TABLE IF NOT EXISTS sales_invoice_items (
	id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
	sales_invoice_id BIGINT NOT NULL,
	product_id BIGINT NOT NULL,
	quantity INT NOT NULL,
	unit_price DECIMAL(12,2) NOT NULL,
	CONSTRAINT fk_invoice_items_invoice
		FOREIGN KEY (sales_invoice_id) REFERENCES sales_invoice(id)
		ON DELETE CASCADE,
	CONSTRAINT fk_invoice_items_product
		FOREIGN KEY (product_id) REFERENCES products(id),
	CONSTRAINT chk_invoice_item_quantity CHECK (quantity > 0),
	CONSTRAINT chk_invoice_item_unit_price CHECK (unit_price >= 0)
);

